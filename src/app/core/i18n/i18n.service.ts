@Injectable({ providedIn: 'root' })
export class I18nService {
  readonly locale = signal<AppLocale>('en');

  t(key: string): string {
    const loc = this.locale();
    return DICT[loc][key] ?? DICT.en[key] ?? key;
  }

  setLocale(loc: AppLocale): void {
    this.locale.set(loc);
  }
}
